#Showing how different types of numbers can have different types in Python
##This should have a type of int - Integer
print(type(10))
##This should have a type of float - Float/Decimal number
print(type(1.1))
##Even though the number is negative doesn't change it's type, will have type int - Integer
print(type(-42))
#This should have a type of str - String
print(type("Hello World"))
#This should have a type of bool - Boolean
print(type(True))
#This should have a type of list
print(type([1,2,3,4]))
#This should have a type of dict - Dictionary
print(type({"Age":18,"Name":"John"}))
#This should have a type of tuple
print(type((1,2,3)))
#This should have a type of set
print(type({1,2,3}))
