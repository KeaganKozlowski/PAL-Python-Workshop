#Concatenation Example
first_name = "John"
last_name = "Smith"
full_name = first_name + last_name
print(full_name) #Should output "JohnSmith"
##Not just limited to two variables
middle_name = "Doe"
actual_full_name = first_name + " " + middle_name + " " + last_name
print(actual_full_name) #Should output "John Doe Smith"

#Repetition Example 
to_repeat = "hello"
print(to_repeat*2) #Should ouput "hellohello"
